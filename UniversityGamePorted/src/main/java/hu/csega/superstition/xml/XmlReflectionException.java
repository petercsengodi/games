package hu.csega.superstition.xml;

public class XmlReflectionException extends Exception {

	public XmlReflectionException() {
	}

	public XmlReflectionException(String message) {
		super(message);
	}

	public XmlReflectionException(Throwable cause) {
		super(cause);
	}

	public XmlReflectionException(String message, Throwable cause) {
		super(message, cause);
	}

	/** Default serial version UID. */
	private static final long serialVersionUID = 1L;

}
