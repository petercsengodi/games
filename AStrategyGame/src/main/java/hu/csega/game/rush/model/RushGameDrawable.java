package hu.csega.game.rush.model;

public class RushGameDrawable {

	private double x;
	private double y;
	private double alfa;

	private double delayBetween;
	private double delayRemaining;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getAlfa() {
		return alfa;
	}

	public void setAlfa(double alfa) {
		this.alfa = alfa;
	}

	public double getDelayBetween() {
		return delayBetween;
	}

	public void setDelayBetween(double delayBetween) {
		this.delayBetween = delayBetween;
	}

	public double getDelayRemaining() {
		return delayRemaining;
	}

	public void setDelayRemaining(double delayRemaining) {
		this.delayRemaining = delayRemaining;
	}

}
